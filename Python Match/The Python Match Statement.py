#Thay vì viết nhiều if..else câu lệnh,có thể sử dụng matchcâu lệnh.
#Câu lệnh này matchchọn một trong nhiều khối mã để thực thi.

day = 4
match day:
  case 1:
    print("Monday")
  case 2:
    print("Tuesday")
  case 3:
    print("Wednesday")
  case 4:
    print("Thursday")
  case 5:
    print("Friday")
  case 6:
    print("Saturday")
  case 7:
    print("Sunday")
